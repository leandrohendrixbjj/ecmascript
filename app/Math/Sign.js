console.clear()

// Static method returns 1 or -1, indicating the sign of the number passed as argument.

console.log(Math.sign(3)) // 1

console.log(Math.sign(-3)) // -1

console.log(Math.sign(0)) // 0

console.log(Math.sign('-3')) // -1

console.log(Math.sign(true)) // 1

console.log(Math.sign(false)) // 0
